// Utility function to create custom variants with configurable parameters
export const createFadeVariant = (
  direction: 'up' | 'down' | 'left' | 'right',
  distance: number = 60,
  duration: number = 0.6
): Variants => {
  const getOffset = () => {
    switch (direction) {
      case 'up': return { y: distance };
      case 'down': return { y: -distance };
      case 'left': return { x: -distance };
      case 'right': return { x: distance };
    }
  };

  return {
    hidden: { opacity: 0, ...getOffset() },
    visible: {
      opacity: 1,
      x: 0,
      y: 0,
      transition: { duration, ease: "easeOut" }
    }
  };
};
